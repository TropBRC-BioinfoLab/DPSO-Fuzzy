def normalize_doid(line: str) -> str | None:
    s = line.strip()
    if not s:
        return None

    # Tangani format DOID_123, DOID:123, atau URI BioPortal/OBO
    match = re.search(r"DOID[:_](\d+)", s)
    if not match:
        return None

    num = match.group(1)

    # Pertahankan leading zero, misalnya DOID:0050685
    return f"DOID:{num}"

def doid_colon_to_uri(doid_colon: str) -> str:
    return f"http://purl.obolibrary.org/obo/{doid_colon.replace(':', '_')}"

def bp_get(url: str, params=None, retry=3):
    require_api_key()
    headers = {"Authorization": f"apikey token={API_KEY}"}

    last_response = None

    for i in range(retry):
        r = requests.get(url, headers=headers, params=params, timeout=60)
        last_response = r

        if r.status_code in (429, 500, 502, 503, 504):
            time.sleep(1.5 * (i + 1))
            continue

        r.raise_for_status()
        return r.json()

    last_response.raise_for_status()

def extract_doid_from_uri(uri: str) -> str | None:
    match = re.search(r"DOID_(\d+)", uri)
    if not match:
        return None
    return f"DOID:{match.group(1)}"

def fetch_descendants(parent_doid_colon: str) -> set[str]:
    parent_uri = quote(doid_colon_to_uri(parent_doid_colon), safe="")
    url = f"{BASE}/ontologies/{ONTOLOGY}/classes/{parent_uri}/descendants"

    descendants = set()
    next_url = url

    while next_url:
        data = bp_get(next_url)

        if isinstance(data, dict):
            items = data.get("collection", [])
            next_url = data.get("links", {}).get("next")
        elif isinstance(data, list):
            items = data
            next_url = None
        else:
            items = []
            next_url = None

        for item in items:
            if not isinstance(item, dict):
                continue

            cid = item.get("@id", "")
            doid = extract_doid_from_uri(cid)

            if doid:
                descendants.add(doid)

    return descendants

def fetch_pref_label(doid_colon: str) -> str | None:
    if doid_colon in _label_cache:
        return _label_cache[doid_colon]

    uri = quote(doid_colon_to_uri(doid_colon), safe="")
    url = f"{BASE}/ontologies/{ONTOLOGY}/classes/{uri}"

    data = bp_get(url)
    label = data.get("prefLabel")

    _label_cache[doid_colon] = label
    return label

def fetch_ancestor_labels(doid_colon: str) -> list[str]:
    if doid_colon in _anc_label_cache:
        return _anc_label_cache[doid_colon]

    uri = quote(doid_colon_to_uri(doid_colon), safe="")
    url = f"{BASE}/ontologies/{ONTOLOGY}/classes/{uri}/ancestors"

    data = bp_get(url)

    if isinstance(data, dict):
        items = data.get("collection", [])
    elif isinstance(data, list):
        items = data
    else:
        items = []

    labels = []

    for item in items:
        if isinstance(item, dict):
            lab = item.get("prefLabel")
            if lab:
                labels.append(lab.strip().lower())

    _anc_label_cache[doid_colon] = labels
    return labels

def matches_generic_label(label: str | None) -> bool:
    if not label:
        return False

    l = label.lower()

    for pat in GENERIC_LABEL_PATTERNS:
        if re.search(pat, l):
            return True

    return False

def has_blacklisted_ancestor(anc_labels: list[str]) -> str | None:
    s = set(anc_labels)

    for bad in BLACKLIST_ANCESTOR_LABELS:
        if bad in s:
            return bad

    return None

