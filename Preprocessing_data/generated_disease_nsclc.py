import requests
import time
import xml.etree.ElementTree as ET
import csv

def fetch_pubmed_ids(term, retmax=100, retstart=0):
    """Ambil daftar PMID dari PubMed berdasarkan istilah pencarian."""
    base = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/esearch.fcgi"
    params = {
        "db": "pubmed",
        "term": term,
        "retmax": retmax,
        "retstart": retstart,
        "retmode": "xml"
    }
    resp = requests.get(base, params=params)
    resp.raise_for_status()
    root = ET.fromstring(resp.text)
    idlist = [id_el.text for id_el in root.findall(".//IdList/Id")]
    return idlist

def fetch_pubmed_article(pmid):
    """Ambil metadata artikel (judul, abstrak, MeSH terms) berdasarkan PMID."""
    base = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/efetch.fcgi"
    params = {
        "db": "pubmed",
        "id": pmid,
        "retmode": "xml",
        "rettype": "abstract"
    }
    resp = requests.get(base, params=params)
    resp.raise_for_status()
    root = ET.fromstring(resp.text)
    art = {}
    art['PMID'] = pmid
    title_el = root.find(".//ArticleTitle")
    art['Title'] = title_el.text if title_el is not None else ""
    abstract_el = root.find(".//Abstract/AbstractText")
    art['Abstract'] = abstract_el.text if abstract_el is not None else ""
    # MeSH terms
    mesh_terms = [el.text for el in root.findall(".//MeshHeading/DescriptorName")]
    art['MeSH'] = "|".join(mesh_terms)
    return art

def main():
    # Istilah pencarian: NSCLC + comorbidity
    term = "(Non-Small Cell Lung Cancer[MeSH Terms] OR NSCLC) AND comorbidities)"
    pmids = fetch_pubmed_ids(term, retmax=2000)
    print(f"Found {len(pmids)} PMIDs.")

    results = []
    for i, pmid in enumerate(pmids):
        try:
            art = fetch_pubmed_article(pmid)
            results.append(art)
        except Exception as e:
            print(f"Error fetching PMID {pmid}: {e}")
        time.sleep(0.34)  # agar tidak kena rate limit

    # Simpan ke CSV
    keys = ['PMID', 'Title', 'Abstract', 'MeSH']
    with open("/content/drive/MyDrive/dataset/Tesis/pubtator/nsclc_comorbidity_pubmed.csv", "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=keys)
        writer.writeheader()
        for art in results:
            writer.writerow({k: art.get(k, "") for k in keys})

    print("Done. CSV tersimpan.")

if __name__ == "__main__":
    main()

