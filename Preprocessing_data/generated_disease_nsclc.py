# 3) Layer 2: explicit blacklist + generic/umbrella filtering + audit
kept_final = []
removed_l2 = []
audit_rows = []

for d in layer1_kept:
    label = fetch_pref_label(d)
    anc_labels = fetch_ancestor_labels(d)

    reason = None

    # Manual blacklist DOID
    if d in EXPLICIT_REMOVE_DOIDS:
        reason = "explicit_remove:manual_blacklist"

    # Depth filter
    if not reason and MIN_ANCESTOR_COUNT > 0 and len(anc_labels) < MIN_ANCESTOR_COUNT:
        reason = f"too_general_low_depth(<{MIN_ANCESTOR_COUNT})"

    # Ancestor branch filter
    bad_anc = has_blacklisted_ancestor(anc_labels)
    if not reason and bad_anc:
        reason = f"blacklisted_ancestor:{bad_anc}"

    # Label regex filter
    if not reason and matches_generic_label(label):
        reason = "generic_label_pattern"

    if reason:
        removed_l2.append(d)
        audit_rows.append([d, label or "", "REMOVED", reason])
    else:
        kept_final.append(d)
