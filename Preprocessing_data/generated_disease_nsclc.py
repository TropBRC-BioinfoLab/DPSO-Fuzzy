import requests

def get_doid(disease_name, api_key):
    url = "http://data.bioontology.org/search"
    params = {
        'q': disease_name,
        'apikey': api_key,
        'ontologies': 'DOID',
        'require_exact_match': 'false'
    }

    response = requests.get(url, params=params)

    if response.status_code == 200:
        try:
            results = response.json()
            collection = results.get('collection', [])
            if collection:
                first_result = collection[0]
                label = first_result.get('prefLabel', disease_name)
                doid_url = first_result.get('@id', '')
                doid = doid_url.split('/')[-1] if doid_url else 'No DOID found'
                return label, doid
            else:
                return disease_name, "No DOID found"
        except ValueError:
            return disease_name, "Failed to decode JSON"
    else:
        return disease_name, f"Error: {response.status_code}"

