def load_pmids_from_csv(input_file):
    """Ambil PMID dari file CSV hasil PubMed."""
    pmids = []

    with open(input_file, "r", encoding="utf-8") as infile:
        sample = infile.read(2048)
        dialect = csv.Sniffer().sniff(sample)
        infile.seek(0)

        reader = csv.DictReader(infile, dialect=dialect)

        for row in reader:
            pmid = row.get("PMID", "").strip()
            if pmid:
                pmids.append(pmid)

    return list(dict.fromkeys(pmids))  # hapus duplikat, tetap urut


def fetch_pubtator_annotations(pmids):
    """
    Ambil anotasi PubTator berdasarkan PMID.
    Format yang digunakan: pubtator.
    """

    url = "https://www.ncbi.nlm.nih.gov/research/pubtator3-api/publications/export/pubtator"

    params = {
        "pmids": ",".join(pmids)
    }

    response = requests.get(url, params=params, timeout=60)
    response.raise_for_status()

    return response.text


def parse_disease_annotations(pubtator_text):
    """
    Ekstrak anotasi bertipe Disease dari format PubTator.

    Format umum baris anotasi PubTator:
    PMID    start    end    mention    type    identifier

    Contoh:
    12345678    10    24    lung cancer    Disease    MESH:D008175
    """

    disease_annotations = []

    for line in pubtator_text.splitlines():
        line = line.strip()

        if not line:
            continue

        parts = line.split("\t")

        # Baris judul/abstrak biasanya formatnya:
        # PMID|t|Title
        # PMID|a|Abstract
        # Jadi dilewati.
        if len(parts) < 5:
            continue

        pmid = parts[0]
        mention = parts[3].strip()
        entity_type = parts[4].strip()

        identifier = ""
        if len(parts) >= 6:
            identifier = parts[5].strip()

        if entity_type.lower() == "disease" and mention:
            disease_annotations.append({
                "PMID": pmid,
                "Disease_Mention": mention,
                "Entity_Type": entity_type,
                "Identifier": identifier
            })

    return disease_annotations


def main():
    pmids = load_pmids_from_csv(input_file)
    print(f"Total PMID ditemukan: {len(pmids)}")

    unique_diseases = set()
    all_annotations = []

    batch_size = 100

    for i in range(0, len(pmids), batch_size):
        batch_pmids = pmids[i:i + batch_size]
        print(f"Memproses batch {i // batch_size + 1}: {len(batch_pmids)} PMID")

        try:
            pubtator_text = fetch_pubtator_annotations(batch_pmids)
            disease_annotations = parse_disease_annotations(pubtator_text)

            for ann in disease_annotations:
                unique_diseases.add(ann["Disease_Mention"])
                all_annotations.append(ann)

        except Exception as e:
            print(f"Error pada batch {i // batch_size + 1}: {e}")

        time.sleep(1)  # jeda agar tidak terlalu sering request

    # Simpan daftar penyakit unik
    with open(output_file, "w", encoding="utf-8") as outfile:
        for disease in sorted(unique_diseases):
            outfile.write(disease + "\n")

    # Simpan detail anotasi penyakit
    with open(output_detail_file, "w", newline="", encoding="utf-8") as outfile:
        fieldnames = ["PMID", "Disease_Mention", "Entity_Type", "Identifier"]
        writer = csv.DictWriter(outfile, fieldnames=fieldnames)
        writer.writeheader()

        for ann in all_annotations:
            writer.writerow(ann)

    print("✅ Selesai.")
    print(f"Total disease mention unik: {len(unique_diseases)}")
    print(f"File penyakit unik: {output_file}")
    print(f"File detail anotasi: {output_detail_file}")


