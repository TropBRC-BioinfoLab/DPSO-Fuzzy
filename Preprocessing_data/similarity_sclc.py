def load_similarity_edges(path):
    df = pd.read_csv(path)

    sim_candidates = [
        c for c in df.columns
        if any(k in c.lower() for k in ["similarity", "sim", "score", "value"])
    ]

    if len(sim_candidates) > 0 and df.shape[1] >= 3:
        sim_col = sim_candidates[0]

        id_cols = [c for c in df.columns if c != sim_col]
        source_col = id_cols[0]
        target_col = id_cols[1]

        edges = df[[source_col, target_col, sim_col]].copy()
        edges.columns = ["source", "target", "similarity"]

    else:
        mat = pd.read_csv(path, index_col=0)
        mat = mat.apply(pd.to_numeric, errors="coerce")

        rows = []
        ids = mat.index.astype(str).tolist()

        for i in range(len(ids)):
            for j in range(i + 1, len(ids)):
                val = mat.iloc[i, j]
                if pd.notna(val):
                    rows.append([ids[i], ids[j], float(val)])

        edges = pd.DataFrame(rows, columns=["source", "target", "similarity"])

    edges["source"] = edges["source"].astype(str)
    edges["target"] = edges["target"].astype(str)
    edges["similarity"] = pd.to_numeric(edges["similarity"], errors="coerce")

    edges = edges.dropna(subset=["source", "target", "similarity"])
    edges = edges[edges["source"] != edges["target"]]

    edges["u"] = edges[["source", "target"]].min(axis=1)
    edges["v"] = edges[["source", "target"]].max(axis=1)

    edges = (
        edges.groupby(["u", "v"], as_index=False)["similarity"]
        .max()
        .rename(columns={"u": "source", "v": "target"})
    )

    return edges


# ------------------------------------------------------------
# 3. Fungsi min-max normalization
# ------------------------------------------------------------
def minmax(series):
    min_val = series.min()
    max_val = series.max()

    if max_val == min_val:
        return pd.Series([1.0] * len(series), index=series.index)

    return (series - min_val) / (max_val - min_val)


