# ==== UTILITAS ====
def normalize_doid_to_iri(doid):
    """
    Mengubah DOID pendek menjadi full IRI BioPortal/OBO.
    Contoh:
    DOID:9352 -> http://purl.obolibrary.org/obo/DOID_9352
    """
    doid = str(doid).strip()

    if doid.startswith("http://") or doid.startswith("https://"):
        return doid

    if doid.startswith("DOID:"):
        number = doid.replace("DOID:", "")
    elif doid.startswith("DOID_"):
        number = doid.replace("DOID_", "")
    else:
        number = doid

    return f"http://purl.obolibrary.org/obo/DOID_{number}"


def iri_to_doid(iri):
    """
    Mengubah full IRI menjadi DOID pendek.
    Contoh:
    http://purl.obolibrary.org/obo/DOID_9352 -> DOID:9352
    """
    iri = str(iri).strip()
    if "DOID_" in iri:
        return "DOID:" + iri.split("DOID_")[-1]
    return iri


def extract_uri(item):
    """
    Mengambil URI class dari response BioPortal.
    """
    if isinstance(item, dict):
        return item.get("@id") or item.get("id")
    return None


def bioportal_get(url, params=None, max_retries=3):
    """
    Request BioPortal dengan retry sederhana.
    """
    if params is None:
        params = {}

    params = dict(params)
    params["apikey"] = api_key
    params["display_context"] = "false"

    for attempt in range(max_retries):
        try:
            response = session.get(url, params=params, timeout=30)

            if response.status_code == 200:
                return response.json()

            print(f"\n⚠️ HTTP {response.status_code} untuk URL: {url}")
            time.sleep(1 + attempt)

        except requests.exceptions.RequestException as e:
            print(f"\n⚠️ Request error: {e}")
            time.sleep(1 + attempt)

    return None


# ==== AMBIL PATH KE ROOT ====
def get_paths_to_root(doid):
    """
    Mengambil semua path dari suatu DOID ke root menggunakan BioPortal.
    Endpoint ini lebih cocok untuk Wang-like similarity karena kita butuh jarak term ke ancestor.
    """
    class_iri = normalize_doid_to_iri(doid)
    encoded_class_iri = quote(class_iri, safe="")

    url = f"{BASE_URL}/ontologies/{ontology}/classes/{encoded_class_iri}/paths_to_root"
    data = bioportal_get(url)

    if data is None:
        return None

    paths = []

    # BioPortal dapat mengembalikan list langsung atau dict berisi collection.
    if isinstance(data, dict) and "collection" in data:
        data = data["collection"]

    if isinstance(data, list):
        for path in data:
            # Format umum: path adalah list of class objects
            if isinstance(path, list):
                uris = [extract_uri(x) for x in path]
                uris = [u for u in uris if u is not None]
                if uris:
                    paths.append(uris)

            # Antisipasi jika format dibungkus dictionary
            elif isinstance(path, dict):
                possible_path = (
                    path.get("classes")
                    or path.get("path")
                    or path.get("collection")
                    or []
                )
                if isinstance(possible_path, list):
                    uris = [extract_uri(x) for x in possible_path]
                    uris = [u for u in uris if u is not None]
                    if uris:
                        paths.append(uris)

    return paths


# ==== WANG-LIKE SEMANTIC CONTRIBUTION ====
def get_wang_contribution(doid, weight=0.8):
    """
    Menghitung semantic contribution untuk satu DOID.

    Prinsip:
    - DOID itu sendiri diberi kontribusi 1.0.
    - Ancestor yang semakin jauh kontribusinya semakin kecil.
    - weight default 0.8, mengikuti praktik umum Wang similarity untuk relasi is_a.
    """
    class_iri = normalize_doid_to_iri(doid)
    paths = get_paths_to_root(doid)

    if paths is None:
        return None

    contribution = {class_iri: 1.0}

    for path in paths:
        if not path:
            continue

        # Cari posisi term utama dalam path.
        # BioPortal bisa mengembalikan urutan root->term atau term->root,
        # sehingga kita gunakan jarak absolut dari posisi term.
        if class_iri in path:
            self_idx = path.index(class_iri)
        else:
            # fallback jika term utama tidak muncul eksplisit
            self_idx = len(path) - 1

        for idx, ancestor_iri in enumerate(path):
            distance = abs(self_idx - idx)
            value = weight ** distance

            # Jika ancestor muncul dari beberapa path, ambil kontribusi terbesar.
            if ancestor_iri not in contribution:
                contribution[ancestor_iri] = value
            else:
                contribution[ancestor_iri] = max(contribution[ancestor_iri], value)

    return contribution


def wang_similarity(doid_a, doid_b, cache, weight=0.8):
    """
    Menghitung Wang-like similarity antara dua DOID.
    """
    if doid_a not in cache:
        cache[doid_a] = get_wang_contribution(doid_a, weight=weight)
        time.sleep(0.2)

    if doid_b not in cache:
        cache[doid_b] = get_wang_contribution(doid_b, weight=weight)
        time.sleep(0.2)

    contrib_a = cache[doid_a]
    contrib_b = cache[doid_b]

    if contrib_a is None or contrib_b is None:
        return np.nan

    terms_a = set(contrib_a.keys())
    terms_b = set(contrib_b.keys())
    common_terms = terms_a & terms_b

    sv_a = sum(contrib_a.values())
    sv_b = sum(contrib_b.values())

    if sv_a == 0 or sv_b == 0:
        return np.nan

    numerator = sum(contrib_a[t] + contrib_b[t] for t in common_terms)
    denominator = sv_a + sv_b

    return numerator / denominator


