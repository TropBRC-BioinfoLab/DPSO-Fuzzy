# ==== FUNGSI MEMBENTUK JARINGAN ====
def build_network_from_similarity(sim_matrix, threshold):
    """
    Membentuk jaringan dari matrix similarity.
    Edge dibuat jika similarity >= threshold.
    Self-loop tidak dimasukkan.
    """
    G = nx.Graph()

    nodes = sim_matrix.index.tolist()
    G.add_nodes_from(nodes)

    for i in range(len(nodes)):
        for j in range(i + 1, len(nodes)):
            node_i = nodes[i]
            node_j = nodes[j]
            sim_value = sim_matrix.loc[node_i, node_j]

            if sim_value >= threshold:
                G.add_edge(node_i, node_j, weight=sim_value)

    return G


# ==== FUNGSI HITUNG METRIK TOPOLOGI ====
def calculate_topology_metrics(G, threshold):
    """
    Menghitung metrik topologi jaringan untuk satu threshold.
    """
    n_nodes = G.number_of_nodes()
    n_edges = G.number_of_edges()

    density = nx.density(G) if n_nodes > 1 else 0

    degrees = dict(G.degree())
    avg_degree = np.mean(list(degrees.values())) if degrees else 0
    max_degree = max(degrees.values()) if degrees else 0
    min_degree = min(degrees.values()) if degrees else 0

    components = list(nx.connected_components(G))
    num_components = len(components)

    if components:
        giant_component = max(components, key=len)
        giant_component_size = len(giant_component)
        giant_component_ratio = giant_component_size / n_nodes
    else:
        giant_component_size = 0
        giant_component_ratio = 0

    isolated_nodes = list(nx.isolates(G))
    num_isolated_nodes = len(isolated_nodes)
    isolated_node_ratio = num_isolated_nodes / n_nodes if n_nodes > 0 else 0

    avg_clustering = nx.average_clustering(G) if n_edges > 0 else 0

    # Average shortest path hanya bisa dihitung jika graph connected.
    # Jika tidak connected, dihitung pada giant component.
    if giant_component_size > 1:
        G_giant = G.subgraph(giant_component).copy()
        avg_shortest_path = nx.average_shortest_path_length(G_giant)
        diameter = nx.diameter(G_giant)
    else:
        avg_shortest_path = 0
        diameter = 0

    # Modularity dihitung menggunakan greedy modularity communities
    if n_edges > 0:
        communities = list(nx.algorithms.community.greedy_modularity_communities(G))
        modularity_score = nx.algorithms.community.modularity(G, communities)
        num_communities = len(communities)
    else:
        modularity_score = 0
        num_communities = 0

    return {
        "threshold": threshold,
        "nodes": n_nodes,
        "edges": n_edges,
        "density": density,
        "avg_degree": avg_degree,
        "min_degree": min_degree,
        "max_degree": max_degree,
        "num_components": num_components,
        "giant_component_size": giant_component_size,
        "giant_component_ratio": giant_component_ratio,
        "num_isolated_nodes": num_isolated_nodes,
        "isolated_node_ratio": isolated_node_ratio,
        "avg_clustering": avg_clustering,
        "avg_shortest_path_giant": avg_shortest_path,
        "diameter_giant": diameter,
        "num_communities": num_communities,
        "modularity": modularity_score
    }


