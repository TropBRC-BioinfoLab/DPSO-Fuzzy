def normalize_membership(raw_membership):
    """
    Mengubah berbagai kemungkinan format membership menjadi:

    {
        node_id: {
            community_id: membership_value,
            ...
        },
        ...
    }
    """

    # Format 1:
    # {
    #   "48": {"2": 0.92, "16": 0.08},
    #   ...
    # }
    if isinstance(raw_membership, dict):
        # Kalau ada wrapper seperti "best_memberships" atau "fuzzy_membership"
        possible_keys = [
            "best_memberships",
            "fuzzy_membership",
            "memberships",
            "membership",
            "node_memberships"
        ]

        for key in possible_keys:
            if key in raw_membership:
                raw_membership = raw_membership[key]
                break

        normalized = {}

        for node, membership_dict in raw_membership.items():

            if isinstance(membership_dict, str):
                membership_dict = ast.literal_eval(membership_dict)

            normalized[str(node)] = {
                str(comm): float(mu)
                for comm, mu in membership_dict.items()
            }

        return normalized

    # Format 2:
    # [
    #   {"node": 48, "community": 2, "membership": 0.92},
    #   {"node": 48, "community": 16, "membership": 0.08}
    # ]
    elif isinstance(raw_membership, list):
        normalized = {}

        for row in raw_membership:
            node = str(row.get("node", row.get("node_id")))
            comm = str(row.get("community", row.get("community_id", row.get("comm"))))
            mu = float(row.get("membership", row.get("mu", row.get("value"))))

            if node not in normalized:
                normalized[node] = {}

            normalized[node][comm] = mu

        return normalized

    else:
        raise ValueError("Format membership tidak dikenali.")


