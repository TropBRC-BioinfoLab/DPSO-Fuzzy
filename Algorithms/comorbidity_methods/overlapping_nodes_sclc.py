# 2. FUNGSI PEMILIH REPRESENTATIVE DISEASE YANG BARU
# =====================================================

def choose_representative_disease_unique_v2(
    comm_id,
    overlap_node,
    fuzzy_membership,
    G,
    stable_nodes,
    used_representatives,
    representative_cache=None,
    alpha_member=0.40,
    allow_relax_threshold=True
):
    """
    Memilih representative disease valid untuk suatu community.

    Prioritas:
    1. Node penyakit valid dengan membership >= alpha_member pada community tersebut.
    2. Jika tidak ada, threshold dilonggarkan menjadi membership > 0.
    3. Jika masih tidak ada, gunakan tetangga overlap_node yang valid.
    4. Jika masih tidak ada, return None.

    Skor pemilihan:
    - membership value
    - degree node dalam G
    - edge weight terhadap overlap_node jika ada
    """

    stable_nodes_set = set(stable_nodes)

    # Jangan langsung pakai cache jika isinya invalid atau sudah digunakan
    cache_key = normalize_comm_key(comm_id)

    if representative_cache is not None and cache_key in representative_cache:
        cached = representative_cache[cache_key]

        if (
            cached is not None
            and not is_invalid_representative_node(cached)
            and cached not in stable_nodes_set
            and cached not in used_representatives
            and cached != overlap_node
            and cached in G.nodes()
        ):
            used_representatives.add(cached)
            return cached

    candidates = []

    # -------------------------------------------------
    # A. Kandidat utama: semua node dalam graph
    # -------------------------------------------------
    for node in G.nodes():

        if node == overlap_node:
            continue

        if node in stable_nodes_set:
            continue

        if node in used_representatives:
            continue

        if is_invalid_representative_node(node):
            continue

        membership = get_membership_value(
            fuzzy_membership=fuzzy_membership,
            node=node,
            comm_id=comm_id
        )

        if membership >= alpha_member:
            degree = G.degree(node)

            edge_weight = 0.0
            if G.has_edge(overlap_node, node):
                edge_weight = G[overlap_node][node].get("weight", 1.0)

            candidates.append({
                "node": node,
                "membership": membership,
                "degree": degree,
                "edge_weight": edge_weight,
                "source": "membership>=alpha"
            })

    # -------------------------------------------------
    # B. Jika tidak ada, longgarkan threshold membership
    # -------------------------------------------------
    if len(candidates) == 0 and allow_relax_threshold:

        for node in G.nodes():

            if node == overlap_node:
                continue

            if node in stable_nodes_set:
                continue

            if node in used_representatives:
                continue

            if is_invalid_representative_node(node):
                continue

            membership = get_membership_value(
                fuzzy_membership=fuzzy_membership,
                node=node,
                comm_id=comm_id
            )

            if membership > 0:
                degree = G.degree(node)

                edge_weight = 0.0
                if G.has_edge(overlap_node, node):
                    edge_weight = G[overlap_node][node].get("weight", 1.0)

                candidates.append({
                    "node": node,
                    "membership": membership,
                    "degree": degree,
                    "edge_weight": edge_weight,
                    "source": "membership>0"
                })

    # -------------------------------------------------
    # C. Jika tetap tidak ada, pakai tetangga overlap_node
    # -------------------------------------------------
    if len(candidates) == 0 and overlap_node in G.nodes():

        for node in G.neighbors(overlap_node):

            if node == overlap_node:
                continue

            if node in stable_nodes_set:
                continue

            if node in used_representatives:
                continue

            if is_invalid_representative_node(node):
                continue

            membership = get_membership_value(
                fuzzy_membership=fuzzy_membership,
                node=node,
                comm_id=comm_id
            )

            degree = G.degree(node)
            edge_weight = G[overlap_node][node].get("weight", 1.0)

            candidates.append({
                "node": node,
                "membership": membership,
                "degree": degree,
                "edge_weight": edge_weight,
                "source": "neighbor_fallback"
            })

    if len(candidates) == 0:
        return None

    candidate_df = pd.DataFrame(candidates)

    # Urutan prioritas:
    # membership terbesar, degree terbesar, edge weight terbesar
    candidate_df = candidate_df.sort_values(
        by=["membership", "degree", "edge_weight"],
        ascending=[False, False, False]
    ).reset_index(drop=True)

    selected = candidate_df.loc[0, "node"]

    used_representatives.add(selected)

    if representative_cache is not None:
        representative_cache[cache_key] = selected

    return selected

