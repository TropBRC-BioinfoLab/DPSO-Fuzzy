def detect_overlapping_nodes(fuzzy_membership, alpha_threshold):
    """
    Node disebut overlapping jika memiliki minimal dua komunitas
    dengan nilai membership >= alpha_threshold.
    """

    rows = []

    for node, membership_dict in fuzzy_membership.items():

        memberships = {
            str(comm): float(mu)
            for comm, mu in membership_dict.items()
        }

        selected = {
            comm: mu
            for comm, mu in memberships.items()
            if mu >= alpha_threshold
        }

        sorted_memberships = sorted(
            memberships.items(),
            key=lambda x: x[1],
            reverse=True
        )

        top1_comm, top1_mu = sorted_memberships[0]

        if len(sorted_memberships) > 1:
            top2_comm, top2_mu = sorted_memberships[1]
        else:
            top2_comm, top2_mu = None, 0.0

        if len(selected) >= 2:
            rows.append({
                "node": node,
                "alpha_threshold": alpha_threshold,
                "num_selected_communities": len(selected),
                "selected_communities": list(selected.keys()),
                "selected_memberships": selected,
                "top1_community": top1_comm,
                "top1_membership": top1_mu,
                "top2_community": top2_comm,
                "top2_membership": top2_mu,
                "membership_gap_top1_top2": top1_mu - top2_mu,
                "all_memberships": memberships
            })

    return pd.DataFrame(rows)

