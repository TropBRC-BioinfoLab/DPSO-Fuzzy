def choose_representative_disease_unique(
    comm_id,
    overlap_node,
    fuzzy_membership,
    G,
    stable_nodes,
    used_representatives,
    representative_cache,
    alpha_member=0.30
):
    comm_id = normalize_comm_id(comm_id)

    if comm_id in representative_cache:
        return representative_cache[comm_id]

    members = get_members_of_internal_community(
        comm_id=comm_id,
        fuzzy_membership=fuzzy_membership,
        alpha_member=alpha_member
    )

    stable_lc = {norm_text(s) for s in stable_nodes}
    used_lc = {norm_text(u) for u in used_representatives}
    overlap_lc = norm_text(overlap_node)

    # daftar label generik yang tidak boleh dijadikan representative
    banned_labels = {
        "cancer",
        "tumor",
        "neoplasm",
        "malignant neoplasm",
        "carcinoma"
    }

    graph_lookup = {
        norm_text(n): n
        for n in G.nodes()
    }

    candidates = []

    for node, mu in members:
        node_lc = norm_text(node)

        # skip stable overlapping nodes
        if node_lc in stable_lc:
            continue

        # skip overlap node itu sendiri
        if node_lc == overlap_lc:
            continue

        # skip representative yang sudah dipakai
        if node_lc in used_lc:
            continue

        # skip label terlalu umum
        if node_lc in banned_labels:
            continue

        if node_lc in graph_lookup:
            actual_node = graph_lookup[node_lc]
            degree = G.degree(actual_node)
        else:
            actual_node = node
            degree = 0

        candidates.append({
            "node": str(actual_node),
            "membership": mu,
            "degree": degree
        })

    if len(candidates) > 0:
        candidates = sorted(
            candidates,
            key=lambda x: (x["degree"], x["membership"]),
            reverse=True
        )

        chosen = candidates[0]["node"]
        used_representatives.add(chosen)
        representative_cache[comm_id] = chosen
        return chosen

    # fallback kalau semua kandidat terfilter
    fallback_label = f"Community {comm_id}"
    representative_cache[comm_id] = fallback_label
    return fallback_label

