# =====================================================
# 1. FILTER NODE NON-DISEASE
# =====================================================

def is_invalid_representative_node(x):
    """
    True jika node bukan nama penyakit valid.
    Contoh yang dibuang:
    - Community 3937
    - C3937
    - C_3937
    """
    x = str(x).strip().lower()

    patterns = [
        r"^community\s*\d+$",
        r"^community\s*_\s*\d+$",
        r"^c\s*\d+$",
        r"^c_\s*\d+$"
    ]

    return any(re.match(p, x) for p in patterns)


def is_valid_disease_node(x):
    x = str(x).strip()

    if x == "":
        return False

    if is_invalid_representative_node(x):
        return False

    return True


def normalize_comm_key(x):
    """
    3937, '3937', 'C3937' -> '3937'
    """
    x = str(x).strip()
    x = re.sub(r"^C", "", x, flags=re.IGNORECASE)
    return x


# =====================================================
# 2. FUNGSI AMBIL MEMBERSHIP
# =====================================================

def get_membership_value(fuzzy_membership, node, comm_id):
    """
    Mendukung beberapa kemungkinan format fuzzy_membership:
    1. fuzzy_membership[node][community]
    2. fuzzy_membership[community][node]
    """

    comm_key = normalize_comm_key(comm_id)
    possible_comm_keys = [
        comm_id,
        str(comm_id),
        comm_key,
        f"C{comm_key}",
        f"Community {comm_key}"
    ]

    # Format: fuzzy_membership[node][community]
    if isinstance(fuzzy_membership, dict) and node in fuzzy_membership:
        val = fuzzy_membership[node]

        if isinstance(val, dict):
            for k in possible_comm_keys:
                if k in val:
                    return float(val[k])

        elif isinstance(val, list):
            try:
                idx = int(comm_key)
                if 0 <= idx < len(val):
                    return float(val[idx])
            except:
                pass

    # Format: fuzzy_membership[community][node]
    if isinstance(fuzzy_membership, dict):
        for ck in possible_comm_keys:
            if ck in fuzzy_membership:
                val = fuzzy_membership[ck]

                if isinstance(val, dict) and node in val:
                    return float(val[node])

    return 0.0


# =====================================================
# 3. SKOR KANDIDAT REPRESENTATIVE DISEASE
# =====================================================

def normalize_series(x):
    x = pd.Series(x, dtype=float)

    if x.max() == x.min():
        return pd.Series(np.ones(len(x)), index=x.index)

    return (x - x.min()) / (x.max() - x.min())


def choose_representative_disease_robust(
    comm_id,
    overlap_node,
    fuzzy_membership,
    G,
    stable_nodes,
    used_representatives=None,
    alpha_member=0.40,
    allow_reuse_as_fallback=True
):
    """
    Memilih representative disease dengan fallback bertingkat.

    Prioritas:
    1. Disease valid dengan membership >= alpha_member.
    2. Disease valid dengan membership > 0.
    3. Tetangga valid dari overlap_node.
    4. Semua disease valid dalam graph.

    Skor:
    - membership value
    - degree
    - edge weight terhadap overlap node
    """

    if used_representatives is None:
        used_representatives = set()

    stable_nodes_set = set(stable_nodes)

    def collect_candidates(mode, exclude_used=True):
        candidates = []

        if mode in ["membership_alpha", "membership_positive", "all_graph"]:
            node_pool = list(G.nodes())

        elif mode == "neighbor":
            if overlap_node not in G.nodes():
                return []
            node_pool = list(G.neighbors(overlap_node))

        else:
            node_pool = []

        for node in node_pool:

            if node == overlap_node:
                continue

            if node in stable_nodes_set:
                continue

            if exclude_used and node in used_representatives:
                continue

            if not is_valid_disease_node(node):
                continue

            membership = get_membership_value(
                fuzzy_membership=fuzzy_membership,
                node=node,
                comm_id=comm_id
            )

            if mode == "membership_alpha" and membership < alpha_member:
                continue

            if mode == "membership_positive" and membership <= 0:
                continue

            degree = G.degree(node)

            edge_weight = 0.0
            if G.has_edge(overlap_node, node):
                edge_weight = G[overlap_node][node].get("weight", 1.0)

            candidates.append({
                "node": node,
                "membership": membership,
                "degree": degree,
                "edge_weight": edge_weight,
                "source": mode
            })

        return candidates

    # Urutan pencarian kandidat
    search_modes = [
        "membership_alpha",
        "membership_positive",
        "neighbor",
        "all_graph"
    ]

    candidates = []

    # Pertama: cari kandidat yang belum pernah dipakai
    for mode in search_modes:
        candidates = collect_candidates(mode, exclude_used=True)
        if len(candidates) > 0:
            break

    # Kedua: kalau tidak ada, izinkan reuse agar node tetap punya representative
    if len(candidates) == 0 and allow_reuse_as_fallback:
        for mode in search_modes:
            candidates = collect_candidates(mode, exclude_used=False)
            if len(candidates) > 0:
                break

    if len(candidates) == 0:
        return None, None

    cand_df = pd.DataFrame(candidates)

    cand_df["membership_norm"] = normalize_series(cand_df["membership"])
    cand_df["degree_norm"] = normalize_series(cand_df["degree"])
    cand_df["edge_weight_norm"] = normalize_series(cand_df["edge_weight"])

    cand_df["score"] = (
        0.50 * cand_df["membership_norm"] +
        0.30 * cand_df["degree_norm"] +
        0.20 * cand_df["edge_weight_norm"]
    )

    cand_df = cand_df.sort_values(
        by=["score", "membership", "degree", "edge_weight"],
        ascending=[False, False, False, False]
    ).reset_index(drop=True)

    selected = cand_df.loc[0, "node"]
    source = cand_df.loc[0, "source"]

    used_representatives.add(selected)

    return selected, source


