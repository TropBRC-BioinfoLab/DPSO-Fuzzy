def is_invalid_representative_node(x):
    """
    True jika node hanya label komunitas, bukan disease node.
    Contoh yang dibuang:
    - Community 3937
    - community 4425
    - C3937
    - C_3937
    """
    x = str(x).strip().lower()

    patterns = [
        r"^community\s*\d+$",
        r"^community\s*_\s*\d+$",
        r"^c\s*\d+$",
        r"^c_\s*\d+$"
    ]

    return any(re.match(p, x) for p in patterns)


def normalize_comm_key(x):
    """
    Menyamakan label community:
    3937, '3937', 'C3937' -> '3937'
    """
    x = str(x).strip()
    x = re.sub(r"^C", "", x, flags=re.IGNORECASE)
    return x


def get_membership_value(fuzzy_membership, node, comm_id):
    """
    Mengambil membership value secara fleksibel.
    Mendukung struktur:
    1. fuzzy_membership[node][comm_id]
    2. fuzzy_membership[comm_id][node]
    """

    comm_key = normalize_comm_key(comm_id)

    # Case 1: fuzzy_membership[node][comm]
    if isinstance(fuzzy_membership, dict) and node in fuzzy_membership:
        val = fuzzy_membership[node]

        if isinstance(val, dict):
            possible_keys = [
                comm_id,
                str(comm_id),
                comm_key,
                f"C{comm_key}"
            ]

            for k in possible_keys:
                if k in val:
                    return float(val[k])

        elif isinstance(val, list):
            try:
                idx = int(comm_key)
                if 0 <= idx < len(val):
                    return float(val[idx])
            except:
                pass

    # Case 2: fuzzy_membership[comm][node]
    if isinstance(fuzzy_membership, dict):
        possible_comm_keys = [
            comm_id,
            str(comm_id),
            comm_key,
            f"C{comm_key}"
        ]

        for ck in possible_comm_keys:
            if ck in fuzzy_membership:
                val = fuzzy_membership[ck]

                if isinstance(val, dict) and node in val:
                    return float(val[node])

    return 0.0


