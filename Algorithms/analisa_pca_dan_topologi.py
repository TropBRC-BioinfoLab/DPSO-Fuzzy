positive_metrics = [
    "ONMI",
    "Omega",
    "Comm_P",
    "Comm_R",
    "Comm_F1"
]
std_metrics = [
    "std_ONMI",
    "std_Omega",
    "std_CommP",
    "std_CommR",
    "std_CommF1"
]


# Salin data numerik
X = df[positive_metrics + std_metrics].copy()


# ===============================
# 3. Standardisasi data
# ===============================
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# ===============================
# 4. PCA
# ===============================
pca = PCA(n_components=1)
pc1 = pca.fit_transform(X_scaled)

# Masukkan hasil PC1 ke dataframe
df["PC1"] = pc1[:, 0]

# ===============================
# 5. Koreksi arah PC1
# ===============================
# Dalam PCA, tanda PC1 bisa positif atau negatif secara arbitrer.
# Supaya nilai PC1 yang lebih besar berarti performa lebih baik,
# arah PC1 disesuaikan dengan rata-rata metrik utama.

quality_mean = df[positive_metrics].mean(axis=1)

