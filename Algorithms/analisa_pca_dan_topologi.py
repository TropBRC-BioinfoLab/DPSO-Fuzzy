def compute_topological_distance(
    topology_df,
    target_dataset,
    reference_datasets,
    topology_features,
    fit_scaler_on_reference=True
):
    topo = topology_df.set_index("Dataset")

    if fit_scaler_on_reference:
        scaler = StandardScaler()
        scaler.fit(topo.loc[reference_datasets, topology_features])

        z_reference = pd.DataFrame(
            scaler.transform(topo.loc[reference_datasets, topology_features]),
            index=reference_datasets,
            columns=topology_features
        )

        z_target = pd.Series(
            scaler.transform(topo.loc[[target_dataset], topology_features])[0],
            index=topology_features,
            name=target_dataset
        )
    else:
        all_datasets = list(reference_datasets) + [target_dataset]

        scaler = StandardScaler()
        z_all = pd.DataFrame(
            scaler.fit_transform(topo.loc[all_datasets, topology_features]),
            index=all_datasets,
            columns=topology_features
        )

        z_reference = z_all.loc[reference_datasets]
        z_target = z_all.loc[target_dataset]

    rows = []

    for ref in reference_datasets:
        distance = np.linalg.norm(z_target - z_reference.loc[ref])

        rows.append({
            "Target": target_dataset,
            "Reference": ref,
            "Topology_Distance": distance
        })

    return pd.DataFrame(rows)


def add_inverse_distance_weight(distance_df, eps=1e-12):
    result = distance_df.copy()

    result["Inverse_Distance"] = 1 / (result["Topology_Distance"] + eps)

    result["Weight"] = (
        result["Inverse_Distance"] /
        result.groupby("Target")["Inverse_Distance"].transform("sum")
    )

    return result


def compute_decision_score_from_references(
    df_perf,
    distance_weight_df,
    target_dataset,
    reference_col="Reference",
    performance_col="PC1_norm"
):
    ref_weights = distance_weight_df[
        distance_weight_df["Target"] == target_dataset
    ].copy()

    temp = df_perf[
        df_perf["Data"].isin(ref_weights[reference_col])
    ][["Data", "Model", performance_col]].copy()

    temp = temp.merge(
        ref_weights[[reference_col, "Topology_Distance", "Weight"]],
        left_on="Data",
        right_on=reference_col,
        how="inner"
    )

    temp["Weighted_Performance"] = temp[performance_col] * temp["Weight"]

    decision = (
        temp.groupby("Model", as_index=False)
        .agg(
            Decision_Score=("Weighted_Performance", "sum")
        )
        .sort_values("Decision_Score", ascending=False)
        .reset_index(drop=True)
    )

    decision["Predicted_Rank"] = decision.index + 1

    return decision


def leave_one_network_out_validation(
    df_perf,
    topology_df,
    benchmark_datasets,
    topology_features,
    performance_col="PC1_norm"
):
    summary_rows = []
    all_rank_rows = []
    all_distance_rows = []

    for target in benchmark_datasets:

        references = [d for d in benchmark_datasets if d != target]

        # Hitung jarak topologi target terhadap jaringan referensi
        distance_df = compute_topological_distance(
            topology_df=topology_df,
            target_dataset=target,
            reference_datasets=references,
            topology_features=topology_features,
            fit_scaler_on_reference=True
        )

        distance_weight_df = add_inverse_distance_weight(distance_df)
        all_distance_rows.append(distance_weight_df)

        # Prediksi peringkat algoritma dari jaringan referensi
        pred_rank = compute_decision_score_from_references(
            df_perf=df_perf,
            distance_weight_df=distance_weight_df,
            target_dataset=target,
            performance_col=performance_col
        )

        # Peringkat aktual pada jaringan target
        actual_rank = (
            df_perf[df_perf["Data"] == target][["Model", performance_col]]
            .copy()
            .sort_values(performance_col, ascending=False)
            .reset_index(drop=True)
        )

        actual_rank["Actual_Rank"] = actual_rank.index + 1
        actual_rank = actual_rank.rename(
            columns={performance_col: "Actual_PC1_norm"}
        )

        # Gabungkan prediksi dan aktual
        compare = pred_rank.merge(
            actual_rank,
            on="Model",
            how="inner"
        )

        compare["Target"] = target

        # Ranking berbasis urutan
        compare["Predicted_Rank_for_Corr"] = compare["Decision_Score"].rank(
            ascending=False,
            method="average"
        )

        compare["Actual_Rank_for_Corr"] = compare["Actual_PC1_norm"].rank(
            ascending=False,
            method="average"
        )

        if len(compare) >= 3:
            rho, pval = spearmanr(
                compare["Predicted_Rank_for_Corr"],
                compare["Actual_Rank_for_Corr"]
            )
        else:
            rho, pval = np.nan, np.nan

        predicted_top1 = compare.sort_values(
            "Predicted_Rank"
        ).iloc[0]["Model"]

        actual_top1 = compare.sort_values(
            "Actual_Rank"
        ).iloc[0]["Model"]

        predicted_top3 = set(
            compare.sort_values("Predicted_Rank")
            .head(3)["Model"]
        )

        actual_top3 = set(
            compare.sort_values("Actual_Rank")
            .head(3)["Model"]
        )

